-- 3) Даёт доступ тестам для всех подразделений
-- 4) Назначает оба теста всем активным сотрудникам с подразделением

SET NAMES utf8mb4;
START TRANSACTION;

-- ====== Параметры ======
SET @chief_username := 'admin';
SET @deadline_days := 30;

SET @test1_title := CONVERT('Пневмоиспытания с использованием пневмопульта испытаний (ППИ)' USING utf8mb4) COLLATE utf8mb4_general_ci;
SET @test2_title := CONVERT('Тестовые вопросы по пневмоиспытаниям двигательной установки' USING utf8mb4) COLLATE utf8mb4_general_ci;

-- ====== Кто создаёт тесты/назначения ======
SET @created_by_id := (
    SELECT e.id FROM t_employee e WHERE e.username = @chief_username LIMIT 1
);
SET @created_by_id := COALESCE(
    @created_by_id,
    (SELECT e2.id FROM t_employee e2 WHERE e2.role = 'CHIEF' AND e2.is_active = true ORDER BY e2.id LIMIT 1),
    (SELECT e3.id FROM t_employee e3 WHERE e3.role = 'ADMIN' AND e3.is_active = true ORDER BY e3.id LIMIT 1),
    1
);

-- ====== Очистка старых данных по этим 2 тестам (для повторного запуска) ======
DELETE s_sel
FROM t_test_session_answer_selected_answers s_sel
JOIN t_test_session_answer s_ans ON s_ans.id = s_sel.test_session_answer_id
JOIN t_test_session s ON s.id = s_ans.test_session_id
JOIN t_test t ON t.id = s.test_id
WHERE t.title IN (@test1_title, @test2_title);

DELETE s_ans
FROM t_test_session_answer s_ans
JOIN t_test_session s ON s.id = s_ans.test_session_id
JOIN t_test t ON t.id = s.test_id
WHERE t.title IN (@test1_title, @test2_title);

DELETE s
FROM t_test_session s
JOIN t_test t ON t.id = s.test_id
WHERE t.title IN (@test1_title, @test2_title);

DELETE tae
FROM t_test_assignment_employee tae
JOIN t_test_assignment ta ON ta.id = tae.assignment_id
JOIN t_test t ON t.id = ta.test_id
WHERE t.title IN (@test1_title, @test2_title);

DELETE ta
FROM t_test_assignment ta
JOIN t_test t ON t.id = ta.test_id
WHERE t.title IN (@test1_title, @test2_title);

DELETE a
FROM t_test_answer a
JOIN t_test_question q ON q.id = a.question_id
JOIN t_test t ON t.id = q.test_id
WHERE t.title IN (@test1_title, @test2_title);

DELETE q
FROM t_test_question q
JOIN t_test t ON t.id = q.test_id
WHERE t.title IN (@test1_title, @test2_title);

DELETE td
FROM t_test_department td
JOIN t_test t ON t.id = td.test_id
WHERE t.title IN (@test1_title, @test2_title);

DELETE FROM t_test WHERE title IN (@test1_title, @test2_title);

